#include<bits/stdc++.h>
using namespace std;

int solve(int n){
        int arr[n];
        for(int i=0; i<n; i++){
            cout<<arr[i]<<" ";
        }
        cout<<endl;
}

int main(){
    int a;
    cin>>a;
    solve(a);
    int b;
    cin>>b;
    solve(b);
    int c;
    cin>>c;
    solve(c);
}